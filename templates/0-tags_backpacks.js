// Adds items to Forestry"s backpacks
ServerEvents.tags("item", e => {

    // Digger
    e.add("forestry:backpack/allow/digger", [])


    // Miner
    e.add("forestry:backpack/allow/miner", [])


    // Forester
    e.add("forestry:backpack/allow/forester", [])


    // Hunter
    e.add("forestry:backpack/allow/hunter", [])


    // Adventurer
    e.add("forestry:backpack/allow/adventurer", [])


    // Builder
    e.add("forestry:backpack/allow/builder", [])

})